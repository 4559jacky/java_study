package com.gn.study;

public class ArithmeticOperator {
	public static void main(String[] args) {
		int su1 = 10;
		int su2 = 3;
		
		System.out.println(su1+su2);
		System.out.println(su1-su2);
		System.out.println(su1*su2);
		System.out.println(su1/su2);
		System.out.println(su1%su2);
	}
}
